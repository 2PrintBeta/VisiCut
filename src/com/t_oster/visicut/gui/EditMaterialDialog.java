  private void thumbnailButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_thumbnailButtonActionPerformed
  {//GEN-HEADEREND:event_thumbnailButtonActionPerformed
    JFileChooser fc = new JFileChooser();
    fc.setAcceptAllFileFilterUsed(false);
    fc.addChoosableFileFilter(new ExtensionFilter(".png", "PNG Files (*.png)"));
    if (this.currentMaterial.getThumbnailPath() != null)
    {
      fc.setSelectedFile(new File(this.currentMaterial.getThumbnailPath()));
      fc.setCurrentDirectory(new File(this.currentMaterial.getThumbnailPath()).getParentFile());
    }
    fc.setDialogType(JFileChooser.OPEN_DIALOG);
    if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
    {
      File thumb = fc.getSelectedFile();
      this.currentMaterial.setThumbnailPath(thumb.getAbsolutePath());
      this.thumbnailButton.setText("<html><table cellpadding=3><tr><td><img width=64 height=64 src=file://" + thumb.getAbsolutePath() + "/></td></tr></table></html>");
    }
  }//GEN-LAST:event_thumbnailButtonActionPerformed

